# Читаем строку из файла
full_text = open('data.txt').readline()

# Разрезаем строку по букве 'F'.
# [1:-1] отбрасывает всё, что было ДО первой F и ПОСЛЕ последней F,
# так как по условию цепочка ОБЯЗАТЕЛЬНО должна начинаться и заканчиваться на F.
fragments_between_f = full_text.split('F')[1:-1]

max_length = 0

# Начинаем с 1, так как учитываем самую первую букву 'F', с которой начнется цепочка
current_length = 1

for segment in fragments_between_f:

    # Условие: между двумя F должно быть не более двух букв 'A'
    if segment.count('A') <= 2:

        # Прибавляем длину фрагмента + 1 (сама буква 'F', которая идет после него)
        current_length += len(segment) + 1

        # Обновляем рекорд
        max_length = max(max_length, current_length)
    else:
        # Если условие по буквам 'A' нарушено, цепочка прерывается.
        # Сбрасываем счетчик к 1 (новая цепочка начнется с буквы 'F', стоящей после текущего фрагмента)
        current_length = 1

print(max_length)