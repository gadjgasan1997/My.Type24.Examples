# Читаем одну строку из файла
text_data = open('data.txt').readline()

max_streak = 0       # Рекордное количество подряд идущих пар
current_streak = 0   # Текущее количество найденных пар
i = 0                # Индекс для обхода строки

# Используем while, чтобы иметь возможность прыгать через символы (i += 2)
# Цикл for не поляет менять значение i руками внутри самого цикла
while i < (len(text_data) - 1):

    # Проверяем, образуют ли текущий и следующий символы нужную пару:
    # Первый символ — 'A' или 'C', а второй — обязательно 'B'
    if (text_data[i] == 'A' or text_data[i] == 'C') and text_data[i+1] == 'B':
        current_streak += 1

        # Обновляем рекорд на каждом шаге
        max_streak = max(max_streak, current_streak)

        # Прыгаем на 2 символа вперед, так как эта пара уже обработана
        i += 2
    else:
        # Если пара не подошла, обнуляем текущую серию
        current_streak = 0

        # Сдвигаемся только на 1 символ, чтобы не пропустить начало новой пары
        i += 1

# Печатаем максимальную длину серии из пар
print(max_streak)